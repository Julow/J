#include "keys.h"

void			key_int(t_j *j, int key)
{
	if (j->line.length > 0)
	{
		ft_dstrpop(&(j->line), -1);
		return ;
	}
	ft_dstradd_char(&(j->line), key);
	j_flush(j);
}
