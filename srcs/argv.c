#include "j.h"

char			*g_default_cmd[] = {"cat", NULL};

t_bool			parse_argv(t_j *j, int argc, char **argv)
{
	j->cmd = (argc <= 1) ? g_default_cmd : argv + 1;
	return (true);
}
